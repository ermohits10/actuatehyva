namespace AutifyDigital\V12Finance\Ui\Component\Listing\Column;

use Magento\Framework\View\Element\UiComponentFactory;
use Magento\Framework\View\Element\UiComponent\ContextInterface;

class OrderIdLink extends \Magento\Ui\Component\Listing\Columns\Column
{
    /**
     * Prepare Data Source
     *
     * @param array $dataSource
     * @return array
     */
    public function prepareDataSource(array $dataSource)
    {
        if (isset($dataSource['data']['items'])) {
            foreach ($dataSource['data']['items'] as & $item) {
                if (isset($item['order_increment_id'])) {
                    $html = "<a href='" . $this->context->getUrl('sales/order/view/',['order_id'=> $item['order_id']]) . "'>";
                    $html .= $item['order_increment_id'];
                    $html .= "</a>";
                    $item['order_increment_id'] = $html;
                }
            }
        }

        return $dataSource;
    }
}
